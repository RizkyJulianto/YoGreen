    private void btnTugasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTugasActionPerformed
        // TODO add your handling code here:
        int idRequest = getSelectedIdRequest();
        if (idRequest == -1) {
            return;
        }

        int indexPetugas = cbPetugas.getSelectedIndex();
        if (indexPetugas == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih petugas terlebih dahulu");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Yakin ingin menugaskan petugas untuk pickup ini?",
                "Konfirmasi Penugasan",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        int idPetugas = listIdPetugas.get(indexPetugas);

        PickupRequestDAO dao = new PickupRequestDAO();
        boolean sukses = dao.tugaskanPickup(idRequest, idPetugas);

        if (sukses) {
            JOptionPane.showMessageDialog(this,
                    "Pickup berhasil ditugaskan");
            loadDataPickup();
        } else {
            JOptionPane.showMessageDialog(this,
                    "Gagal menugaskan pickup");
        }

    }//GEN-LAST:event_btnTugasActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int row = tblPickup.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih data pickup terlebih dahulu");
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(
                this,
                "Apakah yakin ingin menghapus permintaan pickup ini?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION
        );

        if (konfirmasi != JOptionPane.YES_OPTION) {
            return;
        }

        int idRequest = Integer.parseInt(
                tblPickup.getValueAt(row, 0).toString()
        );

        PickupRequestDAO dao = new PickupRequestDAO();
        boolean sukses = dao.deletePickup(idRequest);

        if (sukses) {
            JOptionPane.showMessageDialog(this,
                    "Permintaan pickup berhasil dihapus");
            loadDataPickup();
        } else {
            JOptionPane.showMessageDialog(this,
                    "Gagal menghapus permintaan pickup");
        }

    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnUbahStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahStatusActionPerformed
        // TODO add your handling code here:
        int row = tblPickup.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih data pickup terlebih dahulu");
            return;
        }

        int idRequest = Integer.parseInt(
                tblPickup.getValueAt(row, 0).toString()
        );

        String statusBaru = cbStatus.getSelectedItem().toString();

        int konfirmasi = JOptionPane.showConfirmDialog(
                this,
                "Ubah status pickup menjadi \"" + statusBaru + "\" ?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION
        );

        if (konfirmasi != JOptionPane.YES_OPTION) {
            return;
        }

        PickupRequestDAO dao = new PickupRequestDAO();
        boolean sukses = dao.updateStatusPickup(idRequest, statusBaru);

        if (sukses) {
            JOptionPane.showMessageDialog(this,
                    "Status pickup berhasil diubah");
            loadDataPickup();

            cbStatus.setEnabled(false);
            btnUbahStatus.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(this,
                    "Gagal mengubah status pickup");
        }

    }//GEN-LAST:event_btnUbahStatusActionPerformed

