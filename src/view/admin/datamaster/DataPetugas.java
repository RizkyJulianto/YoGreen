    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String nama = txtNama.getText();
        String username = txtUsername.getText();
        String password = new String(txtPassword.getPassword());
        String alamat = txtAlamatPetugas.getText();
        String nohp = txtNohp1.getText();

        if (nama.isEmpty() || username.isEmpty() || password.isEmpty() || alamat.isEmpty() || nohp.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Data wajib belum diisi",
                    "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        UserDAO userDAO = new UserDAO();

        User user = new User();
        user.setNama(nama);
        user.setUsername(username);
        user.setPassword(password);
        user.setAlamat(alamat);
        user.setNo_hp(nohp);

        boolean success = userDAO.insertPetugas(user);

        if (success) {
            JOptionPane.showMessageDialog(this,
                    "Data Petugas berhasil ditambahkan",
                    "Sukses",
                    JOptionPane.INFORMATION_MESSAGE);

            loadData();
            bersihkan();

        } else {
            JOptionPane.showMessageDialog(this,
                    "Gagal Menambahkan Data",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnTambahActionPerformed

