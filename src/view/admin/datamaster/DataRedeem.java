    private void loadDataPickup() {
        DefaultTableModel model = (DefaultTableModel) tblPickup.getModel();
        model.setRowCount(0);

        RedeemDAO dao = new RedeemDAO();
        ResultSet rs = dao.getAllRedeem();

        try {
            while (rs != null && rs.next()) {
                Object[] row = {
                    rs.getInt("id_redeem"),
                    rs.getString("nama_user"),
                    rs.getString("reward_text"),
                    rs.getString("poin_dipakai"),
                    rs.getString("tanggal_redeem"),
                    rs.getString("status")
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load redeem");
        }

    }

    private int getSelectedIdRequest() {
        int row = tblPickup.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih data pickup terlebih dahulu");
            return -1;
        }
        return Integer.parseInt(
                tblPickup.getValueAt(row, 0).toString()
        );
    }

    public void cetakLaporan() {
        try {
            MessageFormat header
                    = new MessageFormat("Laporan Data Pickup YoGreen");

            MessageFormat footer
                    = new MessageFormat("Halaman {0}");

            tblPickup.print(
                    JTable.PrintMode.FIT_WIDTH,
                    header,
                    footer
            );

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mencetak laporan");
        }
    }

