    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int row = tblPickup.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih data pickup terlebih dahulu");
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(
                this,
                "Apakah yakin ingin menghapus data ini?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION
        );

        if (konfirmasi != JOptionPane.YES_OPTION) {
            return;
        }

        int idRedeem = Integer.parseInt(
                tblPickup.getValueAt(row, 0).toString()
        );

        RedeemDAO dao = new RedeemDAO();
        boolean sukses = dao.deleteRedeem(idRedeem);

        if (sukses) {
            JOptionPane.showMessageDialog(this,
                    "Data berhasil dihapus");
            loadDataPickup();
        } else {
            JOptionPane.showMessageDialog(this,
                    "Gagal menghapus data");
        }

    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnUbahStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahStatusActionPerformed
        // TODO add your handling code here:
        int row = tblPickup.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih data penukaran terlebih dahulu");
            return;
        }

        int idRequest = Integer.parseInt(
                tblPickup.getValueAt(row, 0).toString()
        );

        String statusBaru = cbStatus.getSelectedItem().toString();

        int konfirmasi = JOptionPane.showConfirmDialog(
                this,
                "Ubah status penukaran menjadi \"" + statusBaru + "\" ?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION
        );

        if (konfirmasi != JOptionPane.YES_OPTION) {
            return;
        }

        RedeemDAO dao = new RedeemDAO();
        boolean sukses = dao.updateStatusRedeem(idRequest, statusBaru);

        if (sukses) {
            JOptionPane.showMessageDialog(this,
                    "Status penukaran berhasil diubah");
            loadDataPickup();

            cbStatus.setEnabled(false);
            btnUbahStatus.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(this,
                    "Gagal mengubah status penukaran");
        }

    }//GEN-LAST:event_btnUbahStatusActionPerformed

