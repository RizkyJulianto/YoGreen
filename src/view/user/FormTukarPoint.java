    private void btnUbah1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbah1ActionPerformed
        // TODO add your handling code here:
        if (selectedRedeemId == -1 || selectedStatus == null) {
            JOptionPane.showMessageDialog(this,
                    "Pilih data penukaran terlebih dahulu");
            return;
        }

        if (!"diproses".equalsIgnoreCase(selectedStatus)) {
            JOptionPane.showMessageDialog(this,
                    "Data tidak dapat diubah karena sudah diproses admin");
            return;
        }

        if (cbReward.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih hadiah terlebih dahulu");
            return;
        }

        String reward = cbReward.getSelectedItem().toString();
        int poinBaru = ambilPoinDariReward(reward);

        if (poinBaru <= 0) {
            JOptionPane.showMessageDialog(this,
                    "Hadiah tidak valid");
            return;
        }

        int poinUser = Sessions.user.getPoin();

        if (poinUser < poinBaru) {
            JOptionPane.showMessageDialog(this,
                    "EcoPoint tidak mencukupi\n"
                    + "Poin Anda : " + poinUser + "\n"
                    + "Poin Dibutuhkan : " + poinBaru);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Yakin ingin mengubah penukaran hadiah?\n\n"
                + "Hadiah : " + reward + "\n"
                + "Poin   : " + poinBaru,
                "Konfirmasi Ubah Penukaran",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        RedeemDAO dao = new RedeemDAO();
        boolean sukses = dao.updateRedeemUser(
                selectedRedeemId,
                reward,
                poinBaru
        );

        if (sukses) {
            JOptionPane.showMessageDialog(this,
                    "Data penukaran berhasil diperbarui");
            loadData();
            resetSelection();
        } else {
            JOptionPane.showMessageDialog(this,
                    "Gagal memperbarui data");
        }


    }//GEN-LAST:event_btnUbah1ActionPerformed

    private void btnKonfirmasi2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKonfirmasi2ActionPerformed
        // TODO add your handling code here:
        int idUser = Sessions.user.getId_user();
        int poinUser = Sessions.user.getPoin();

        if (cbReward.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Pilih hadiah terlebih dahulu");
            return;
        }

        String reward = cbReward.getSelectedItem().toString();

        int poinDipakai = ambilPoinDariReward(reward);

        if (poinDipakai <= 0) {
            JOptionPane.showMessageDialog(this, "Hadiah tidak valid");
            return;
        }

        if (poinDipakai > poinUser) {
            JOptionPane.showMessageDialog(this, "Poin tidak mencukupi");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Tukar poin sebanyak " + poinDipakai + " ?",
                "Konfirmasi Penukaran",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        boolean sukses = RedeemDAO.tukarPoin(idUser, reward, poinDipakai);

        if (sukses) {
            JOptionPane.showMessageDialog(this, "Penukaran berhasil, menunggu diproses admin");

            Sessions.user.setPoin(poinUser - poinDipakai);

            lblPoin.setText(String.valueOf(Sessions.user.getPoin()));
            loadData();

        } else {
            JOptionPane.showMessageDialog(this, "Penukaran gagal");
        }


    }//GEN-LAST:event_btnKonfirmasi2ActionPerformed

