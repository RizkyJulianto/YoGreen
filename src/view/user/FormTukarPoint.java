    private void tampilPoin() {
        if (Sessions.user != null) {
            lblPoin.setText(String.valueOf(Sessions.user.getPoin()));
        }
    }

    private int ambilPoinDariReward(String reward) {
        int start = reward.lastIndexOf("(");
        int end = reward.lastIndexOf(")");

        String poin = reward.substring(start + 1, end);
        return Integer.parseInt(poin);

    }

    private void loadData() {
        int idUser = Sessions.user.getId_user();

        DefaultTableModel model = (DefaultTableModel) tblRedeem.getModel();
        model.setRowCount(0);

        RedeemDAO dao = new RedeemDAO();
        ResultSet rs = dao.getRedeemByUser(idUser);

        try {
            while (rs != null && rs.next()) {
                Object[] row = {
                    rs.getInt("id_redeem"),
                    rs.getString("reward_text"),
                    rs.getInt("poin_dipakai"),
                    rs.getTimestamp("tanggal_redeem"),
                    rs.getString("status")
                };
                model.addRow(row);
            }

            // jika kosong
            if (model.getRowCount() == 0) {
                lblInfo.setText("Belum ada penukaran poin");
                lblInfo.setVisible(true);
            } else {
                lblInfo.setVisible(false);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data penukaran poin");
            e.printStackTrace();
        }

    }

    private void resetSelection() {
        selectedRedeemId = -1;
        selectedStatus = null;
        selectedPoin = 0;

    }
